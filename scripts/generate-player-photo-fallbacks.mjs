import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, extname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const cacheFile = join(repoRoot, 'scraped-cache', 'json', 'player-photo-resolutions.json');
const privatePlayersDir = join(repoRoot, 'private-assets', 'players');
const staticPlayersDir = join(repoRoot, 'apps', 'web', 'static', 'player-photos');
const targetFile = join(repoRoot, 'apps', 'web', 'src', 'generated', 'playerPhotos.ts');
const allowedExts = new Set(['jpg', 'jpeg', 'png', 'webp']);

if (!existsSync(cacheFile) && existsSync(targetFile)) {
  console.log('[generate-player-photo-fallbacks] cache missing; leaving existing generated file untouched.');
  process.exit(0);
}

const downloaded = readDownloadedPhotoExts();
const fallbacks = readResolvedFallbacks();

writeGeneratedFile(downloaded, fallbacks);

console.log(
  `[generate-player-photo-fallbacks] wrote ${Object.keys(downloaded).length} local entries and ${Object.keys(fallbacks).length} remote fallbacks.`,
);

function readDownloadedPhotoExts() {
  const entries = {};
  for (const dir of [privatePlayersDir, staticPlayersDir]) {
    if (!existsSync(dir)) continue;
    for (const file of readdirSync(dir)) {
      const ext = extname(file).slice(1).toLowerCase();
      if (!allowedExts.has(ext)) continue;
      const id = file.slice(0, -ext.length - 1);
      if (id) entries[id] = ext;
    }
  }
  return sortRecord(entries);
}

function readResolvedFallbacks() {
  if (!existsSync(cacheFile)) return {};
  const raw = JSON.parse(readFileSync(cacheFile, 'utf8'));
  if (!Array.isArray(raw)) throw new Error(`${cacheFile} must contain an array.`);

  const entries = {};
  for (const item of raw) {
    if (!item || item.status !== 'resolved' || !item.playerId || !item.sourceUrl) continue;
    const ext = inferExt(item.filename || item.sourceUrl);
    entries[item.playerId] = {
      src: item.sourceUrl,
      ext,
      page: item.pageUrl || undefined,
    };
  }
  return sortRecord(entries);
}

function inferExt(value) {
  const pathname = (() => {
    try {
      return new URL(value).pathname;
    } catch {
      return value;
    }
  })();
  const ext = extname(pathname).slice(1).toLowerCase();
  return allowedExts.has(ext) ? ext : 'jpg';
}

function sortRecord(record) {
  return Object.fromEntries(Object.entries(record).sort(([a], [b]) => a.localeCompare(b)));
}

