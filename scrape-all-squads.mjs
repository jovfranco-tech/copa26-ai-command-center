import * as cheerio from 'cheerio';
import fs from 'fs';

const TEAMS = {
  ARG: 'Argentina', FRA: 'France', ESP: 'Spain', ENG: 'England', BRA: 'Brazil', POR: 'Portugal', NED: 'Netherlands', BEL: 'Belgium', GER: 'Germany', CRO: 'Croatia', URU: 'Uruguay', COL: 'Colombia', MEX: 'Mexico', USA: 'United_States', CAN: 'Canada', JPN: 'Japan', KOR: 'South_Korea', MAR: 'Morocco', SEN: 'Senegal', EGY: 'Egypt', SUI: 'Switzerland', ECU: 'Ecuador', NOR: 'Norway', TUR: 'Turkey', SWE: 'Sweden', SCO: 'Scotland', AUT: 'Austria', ALG: 'Algeria', GHA: 'Ghana', IRN: 'Iran', KSA: 'Saudi_Arabia', AUS: 'Australia', CZE: 'Czech_Republic', PAR: 'Paraguay', CIV: 'Ivory_Coast', RSA: 'South_Africa', QAT: 'Qatar', TUN: 'Tunisia', NZL: 'New_Zealand', BIH: 'Bosnia_and_Herzegovina', IRQ: 'Iraq', JOR: 'Jordan', UZB: 'Uzbekistan', COD: 'DR_Congo', PAN: 'Panama', CPV: 'Cape_Verde', HAI: 'Haiti', CUW: 'Curaçao'
};

const mapPosition = (pos) => {
  if (pos.includes('GK')) return 'GK';
  if (pos.includes('DF')) return 'DF';
  if (pos.includes('MF')) return 'MF';
  if (pos.includes('FW')) return 'FW';
  return 'MF'; // default
};

async function scrapeTeam(code, country) {
  const url = `https://en.wikipedia.org/wiki/${country}_national_football_team`;
  try {
    const res = await fetch(url);
    const html = await res.text();
    const $ = cheerio.load(html);
    
    let squadTable = null;
    $('h3').each((i, el) => {
      if ($(el).text().includes('Current squad') || $(el).text().includes('Recent call-ups')) {
        let next = $(el).nextAll('table.sortable').first();
        if (next.length) {
          squadTable = next;
          return false;
        }
      }
    });

    if (!squadTable) {
      // Fallback
      $('table.sortable').each((i, el) => {
        if ($(el).find('th').text().includes('Pos.')) {
          squadTable = $(el);
          return false;
        }
      });
    }

    if (!squadTable) {
      console.log(`[${code}] No table found`);
      return null;
    }

    const players = [];
    squadTable.find('tbody tr').each((i, row) => {
      const tds = $(row).find('td, th');
      if (tds.length >= 5) {
        const noMatch = $(tds[0]).text().trim().match(/\d+/);
        const no = noMatch ? parseInt(noMatch[0]) : Math.floor(Math.random() * 99) + 1;
        const posText = $(tds[1]).text().trim();
        const pos = mapPosition(posText);
        let name = $(tds[2]).find('a').first().text().trim();
        if (!name) name = $(tds[2]).text().trim();
        
        let ageText = $(tds[3]).text();
        const ageMatch = ageText.match(/age\s(\d+)/);
        const age = ageMatch ? parseInt(ageMatch[1]) : 25;
        
        let club = $(tds[tds.length - 1]).text().trim();
        if (!club) club = 'Unknown Club';
        
        if (name && name !== 'Player' && !name.includes('Pos.')) {
          players.push([name, pos, club, age, no]);
        }
      }
    });
    
    const cleanPlayers = players.map(p => {
      p[0] = p[0].replace(/\[.*?\]/g, '').replace(/\(c\)/g, '').trim();
      p[2] = p[2].replace(/\[.*?\]/g, '').trim();
      return p;
    }).slice(0, 26);

    console.log(`[${code}] Scraped ${cleanPlayers.length} players`);
    return cleanPlayers.length >= 11 ? cleanPlayers : null;
  } catch (err) {
    console.error(`[${code}] Error:`, err.message);
    return null;
  }
}

async function main() {
  const result = {};
  const content = fs.readFileSync('packages/shared/src/data/squads.ts', 'utf-8');
  let oldSquads = {};
  const match = content.match(/export const SQUADS: Record<string, SquadEntry\[\]> = (\{.*?\});/s);
  if (match) {
    oldSquads = eval('(' + match[1] + ')');
  }

  for (const [code, country] of Object.entries(TEAMS)) {
    let scraped = await scrapeTeam(code, country);
    if (!scraped) {
      scraped = oldSquads[code] || [];
    }
    result[code] = scraped;
  }

  const out = `/**
 * AUTOGENERATED VIA WIKIPEDIA SCRAPER.
 * Contains up to 26 current players per team.
 */
export type SquadEntry = [string, 'GK' | 'DF' | 'MF' | 'FW', string, number, number];

export const SQUADS: Record<string, SquadEntry[]> = ${JSON.stringify(result, null, 2)};
`;

  fs.writeFileSync('packages/shared/src/data/squads.ts', out);
  console.log('✅ Updated packages/shared/src/data/squads.ts');
}

main();
